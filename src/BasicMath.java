class BasicMath
{

    public int sum(int a, int b)
    {
        return a + b;
    }
    public double devide(int a, int b)
    {
        return (a * 1.0) / (b * 1.0);
    }
    public int subtract(int a, int b)
    {
        return a - b;
    }
	public int multiply(int a, int b){
		return a * b;
	}
}
